		public void PrintBranches(Context context)
		{
			PrintBranches(context, "Unconditional branches", b => b.HasCondition is false);
			PrintBranches(context, "Conditional branches", b => b.HasCondition);
		}
		public bool IncludeBranch(Context context, IControlFlowBidirectionalBranch branch)
		{
			if (context.Settings.Optimise is false)
				return true;

			return IncludeBlock(context, branch.From);
		}
		public void PrintBranches(Context context, string header, Predicate<IControlFlowBidirectionalBranch> predicate)
		{
			IReadOnlyList<IControlFlowBidirectionalBranch> branches = context.Graph.Branches
				.Where(predicate.Invoke)
				.Where(b => IncludeBranch(context, b))
				.ToArray();

			PrintBranches(context, header, branches);
		}
		public void PrintBranches(Context context, string header, IReadOnlyList<IControlFlowBidirectionalBranch> branches)
		{
			if (branches.Count is 0)
				return;

			context.Writer.WriteLine();
			context.Writer.WriteLine($"%% {header}");

			foreach (IControlFlowBidirectionalBranch branch in branches)
				PrintBranch(context, branch);
		}
		public void PrintBranch(Context context, IControlFlowBidirectionalBranch branch)
		{
			IControlFlowBlock target = GetTarget(context, branch);

			if (branch.HasCondition is false)
			{
				context.Writer.WriteLine($"{branch.From.Id} --> {target.Id}");
				return;
			}

			using (PrintBranch(context, branch.From.Id, target.Id, BranchKind.SingleArrow))
			{
				TextFragment value = new(branch.IsNegated ? "false" : "true", ClassificationKind.Boolean);
				TextFragmentLineCollection lines = [new(null, value)];
				PrintCode(context, lines);
			}
		}

		public BranchScope PrintBranch(Context context, string fromId, string targetId, BranchKind kind)
		{
			PrintBranchStart(context, fromId, kind);
			return new(context, targetId);
		}
		public BranchScope PrintBranch(Context context, string fromId, string targetId, string arrow)
		{
			PrintBranchStart(context, fromId, arrow);
			return new(context, targetId);
		}
		public void PrintBranchStart(Context context, string id, BranchKind kind)
		{
			string arrow = kind switch
			{
				BranchKind.SingleArrow => "-->",

				_ => ThrowHelper.ThrowArgumentException<string>(nameof(kind), $"Unsupported branch kind ({kind})."),
			};

			PrintBranchStart(context, id, arrow);
		}
		public void PrintBranchStart(Context context, string id, string arrow)
		{
			context.Writer.Write(id);
			context.Writer.Write(' ');
			context.Writer.Write(arrow);
			context.Writer.Write('|');
			context.Writer.Write('"');
		}
		public void PrintBranchEnd(Context context, string id)
		{
			context.Writer.Write('"');
			context.Writer.Write('|');
			context.Writer.WriteLine(id);
		}
