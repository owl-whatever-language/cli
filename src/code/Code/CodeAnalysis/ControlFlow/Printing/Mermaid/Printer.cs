					context.Writer.WriteLine("fontFamily: 'Droid Sans Mono, monospace'");

					string? background = context.Settings.Styles?.Background?.ToHex;

					if (background is not null)
						context.Writer.WriteLine($"edgeLabelBackground: '{background}'");
				}
			}
		}
