		#region Html attribute methods
		public string GetLineId(Context context, int line, bool isDeclaration)
		{
			if (isDeclaration)
				return $"line-source-{line}";

			return $"line-use-{line}";
		}
		public string GetLineIdAttribute(Context context, int line)
		{
			string id = GetLineId(context, line, context.CodeContext is CodeContext.SourceReference);
			return $"id=\"{id}\"";
		}
		public bool TryGetLineLinkAttribute(Context context, TextFragment fragment, [NotNullWhen(true)] out string? attribute)
		{
			attribute = default;

			if (context.CodeContext is not CodeContext.SourceReference and not CodeContext.Graph)
				return false;

			if (fragment.Classification != ClassificationKind.LineNumber)
				return false;

			if (int.TryParse(fragment.Text, out int line))
			{
				attribute = GetLineLinkAttribute(context, line);
				return true;
			}

			return false;
		}
		public string GetLineLinkAttribute(Context context, int line)
		{
			string id = GetLineId(context, line, context.CodeContext is CodeContext.Graph);
			return $"href=\"#{id}\"";
		}
		public string GetLineDataAttribute(Context context, int line) => $"data-line-number=\"{line}\"";

		public bool TryGetSymbolDataAttribute(Context context, TextFragment fragment, [NotNullWhen(true)] out string? attribute)
		{
			return TryGetSymbolDataAttribute(context, fragment.Symbol, out attribute);
		}
		public bool TryGetSymbolDataAttribute(Context context, ISymbol? symbol, [NotNullIfNotNull(nameof(symbol)), NotNullWhen(true)] out string? attribute)
		{
			if (symbol is null)
			{
				attribute = default;
				return false;
			}

			attribute = $"data-symbol-id=\"{symbol.Id}\"";
			return true;
		}
		#endregion

		#region Helpers
		public IControlFlowBlock GetTarget(Context context, IControlFlowOutgoingBranch branch)
		{
			if (context.Settings.Optimise is false)
				return branch.To;

			IControlFlowBlock to = branch.To;

			while (IncludeBlock(context, to) is false && to.Outgoing.Count is 1)
				to = to.Outgoing[0].To;

			return to;
		}
		#endregion
	}
}

