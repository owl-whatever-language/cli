		#region Reference source methods
		public DiagnosticSourceDisplay GetReferenceSource(Context context)
		{
			IClassificationStyling styling = context.Settings.Styles ?? new ClassificationStyling();
			DiagnosticSourceDisplay display = new(context.Graph.Node.GetTree(), styling);

			void Relevant(params ReadOnlySpan<IAnnotatedSyntaxNode> nodes)
			{
				foreach (var node in nodes)
					display.MarkRelevant(node, shouldDim: false);
			}
			void Dimmed(params ReadOnlySpan<IAnnotatedSyntaxNode> nodes)
			{
				foreach (var node in nodes)
					display.MarkRelevant(node, shouldDim: true);
			}

			if (context.Graph is IFunctionControlFlowGraph function)
			{
				Dimmed(function.Node.Signature);

				if (function.Node.Body is IAnnotatedShortFunctionBodySyntax shortBody)
					Dimmed(shortBody.Arrow, shortBody.Terminator);
				else if (function.Node.Body is IAnnotatedBlockFunctionBodySyntax block)
					Dimmed(block.Block.Start, block.Block.End);
			}

			foreach (IControlFlowBlock block in context.Graph.Blocks)
			{
				if (block is IControlFlowExpressionBlock expression)
					Relevant(expression.Expression);
				else if (block is IControlFlowStatementBlock statement)
				{
					foreach (IAnnotatedStatementSyntax current in statement.Statements)
					{
						if (current.IsExecutable)
							Relevant(current);
					}
				}
				else if (block is IControlFlowConstructBlock construct)
				{
					if (construct.Node is IAnnotatedBlockStatementSyntax blockStatement)
						Dimmed(blockStatement.Start, blockStatement.End);
					else if (construct.Node is IAnnotatedWhileStatementSyntax @while)
					{
						Relevant(@while.Keyword);
						Dimmed(@while.Start, @while.End);
					}
					else if (construct.Node is IAnnotatedIfStatementSyntax @if)
					{
						Relevant(@if.Keyword);
						Dimmed(@if.Start, @if.End);
					}
					else if (construct.Node is IAnnotatedIfElseStatementSyntax ifElse)
					{
						Relevant(ifElse.Keyword, ifElse.Else);
						Dimmed(ifElse.Start, ifElse.End);
					}
				}
			}

			display.ApplyTypicalTransformations();
			return display;
		}
		public void PrintReferenceSource(Context context, DiagnosticSourceDisplay source)
		{
			TextFragmentLineCollection lines = source.GetLines();
			lines.PrefixLineMargin();
			source.ApplyDim();

			context = context.WithNewCodeContext(CodeContext.SourceReference);

			context.Writer.WriteLine();
			context.Writer.WriteLine("%% Source code reference");
			using (PrintNode(context, "source_code_reference", NodeKind.Rectangle))
			{
				TextFragment title = new("Source code reference", ClassificationKind.Keyword);
				TextFragment file = new(context.Graph.Node.GetTree().Source.SimpleName, ClassificationKind.File);
				TextFragmentLineCollection header = [new(null, title), new(null, file)];

				PrintCode(context, header, TextAlign.Center);
				context.Writer.WriteLine();
				PrintCode(context, lines);
			}
		}
