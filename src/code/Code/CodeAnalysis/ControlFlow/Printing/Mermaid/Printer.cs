		#region Methods
		public Result Print(IControlFlowGraph graph, Settings settings)
		{
			if (settings.UseInlineCss is false)
				ThrowHelper.ThrowNotSupportedException("The Mermaid control printer doesn't support external css yet.");

			using (StringWriter stringWriter = new())
			using (IndentedTextWriter writer = new(stringWriter, "  "))
			{
				Context context = new(this, graph, settings, writer, CodeContext.Graph);

				PrintFrontMatter(context);
				PrintPreamble(context);

				DiagnosticSourceDisplay source = GetReferenceSource(context);

				if (settings.IncludePlainSource)
					PrintPlainSource(context, source.GetLines());

				writer.WriteLine();
				writer.WriteLine("flowchart TB;");

				using (writer.Indented())
				{
					PrintBlocks(context);
					PrintBranches(context);
				}

				if (settings.IncludeSourceReference)
					PrintReferenceSource(context, source);

				string code = stringWriter.ToString();
				string configCode = GetConfigCode(context);
				string styleCode = GetStyleCode(context);

				return new(graph, settings, code, configCode, styleCode);
			}
		}
		public string GetConfigCode(Context context)
		{
			return $"{{ \"maxTextSize\": {context.Settings.MaxTextSize} }}";
		}
		public string GetStyleCode(Context context)
		{
			return
$@"span:target {{
	scroll-margin-top: calc(50vh - 1em);
	border: 1px solid {context.Settings.Styles?.Get(ClassificationKind.Focus).Color?.ToHtml ?? "#red"};
	padding: 3px;
	margin: -4px;
}}";
		}
		public void PrintPreamble(Context context)
		{
			context.Writer.WriteLine("%% ");
			context.Writer.WriteLine("%% This is a control flow graph that was generated by the OWL compiler.");
			context.Writer.WriteLine("%% https://github.com/owl-whatever-language");
			context.Writer.WriteLine("%% ");

			string file = context.Graph.Node.GetTree().Source.SimpleName;

			if (context.Graph is IDocumentControlFlowGraph)
				context.Writer.WriteLine($"%% This particular CFG was generated from the main code in the {file} file.");
			else if (context.Graph is IFunctionControlFlowGraph function)
				context.Writer.WriteLine($"%% This particular CFG was generated for the '{function.Node.Function.Name}' function in the {file} file.");
			else
				context.Writer.WriteLine($"%% This particular CFG was generated from some part of the {file} file.");

			context.Writer.WriteLine("%% ");
		}
		public void PrintPlainSource(Context context, TextFragmentLineCollection source)
		{
			context.Writer.WriteLine("%% Since this file is most likely going to be very difficult to be human-readable,");
			context.Writer.WriteLine("%% here is a plain text version of the file for:");
			context.Writer.WriteLine("%% ");

			string tab = context.Settings.TabSize is null ? "\t" : new(' ', context.Settings.TabSize.Value);
			using (IndentedTextWriter writer = new(context.Writer, tab))
			{
				for (int i = 0; i < source.Count; i++)
				{
					TextFragmentLine line = source[i];

					if (line.Line is null)
						continue;

					writer.Write("%% ");
					line.WritePlainText(context.Writer);
					writer.WriteLine();
				}
			}

			context.Writer.WriteLine("%% ");
		}
