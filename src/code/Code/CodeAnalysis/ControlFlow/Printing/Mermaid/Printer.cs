		#region Block methods
		public void PrintBlocks(Context context)
		{
			context.Writer.WriteLine("");
			context.Writer.WriteLine("%% Start");
			PrintBlock(context, context.Graph.Start);

			context.Writer.WriteLine("");
			context.Writer.WriteLine("%% End");
			PrintBlock(context, context.Graph.End);

			PrintBlocks<IControlFlowStatementBlock>(context, "Statements");
			PrintBlocks<IControlFlowExpressionBlock>(context, "Expressions");
			PrintBlocks<IControlFlowConstructBlock>(context, "Constructs");
		}
		public bool IncludeBlock(Context context, IControlFlowBlock block)
		{
			if (block is IControlFlowConstructBlock construct)
			{
				if (construct.Expression is null)
					return false;

				if (construct.Expression.WillBranch)
					return true;

				return false;
			}

			if (block is IControlFlowMarkerBlock)
				return false;

			return true;
		}
		public void PrintBlocks<T>(Context context, string header) where T : notnull, IControlFlowBlock
		{
			IReadOnlyList<IControlFlowBlock> blocks = context.Graph.Blocks
				.Where(b => b is T && IncludeBlock(context, b))
				.ToArray();

			PrintBlocks(context, header, blocks);
		}
		public void PrintBlocks(Context context, string header, IReadOnlyList<IControlFlowBlock> blocks)
		{
			if (blocks.Count is 0)
				return;

			context.Writer.WriteLine();
			context.Writer.WriteLine($"%% {header}");

			foreach (IControlFlowBlock block in blocks)
				PrintBlock(context, block);
		}
		public void PrintBlock(Context context, IControlFlowBlock block)
		{
			if (block is IControlFlowStartBlock start)
				PrintBlock(context, start);
			else if (block is IControlFlowEndBlock end)
				PrintBlock(context, end);
			else if (block is IControlFlowExpressionBlock expression)
				PrintBlock(context, expression);
			else if (block is IControlFlowStatementBlock statement)
				PrintBlock(context, statement);
			else if (block is IControlFlowConstructBlock construct)
				PrintBlock(context, construct);
			else if (block is IControlFlowMarkerBlock marker)
				PrintBlock(context, marker);
			else
				ThrowHelper.ThrowArgumentException(nameof(block), $"Unsupported block type ({block.GetType().Name}).");
		}
		public void PrintBlock(Context context, IControlFlowStartBlock block)
		{
			context = context.WithNewCodeContext(CodeContext.StartHeader);
			using (PrintNode(context, block.Id, NodeKind.Rounded))
			{
				TextFragmentLineCollection header = [];

				TextFragment start = new("start", ClassificationKind.Keyword);
				header.AddLine(start);

				if (block.Graph is IDocumentControlFlowGraph document && document.Node.Tree is not null)
				{
					TextFragment file = new(document.Node.Tree.Source.SimpleName, ClassificationKind.File);
					header.AddLine(file);
				}

				PrintCode(context, header, TextAlign.Center);

				if (block.Graph is IFunctionControlFlowGraph function)
				{
					TextFragmentLineCollection signature = function.Node.Signature
						.GetLines()
						.TrimAll()
						.PrefixLineMargin();

					context.Writer.WriteLine();
					PrintCode(context, signature);
				}
			}
		}
		public void PrintBlock(Context context, IControlFlowEndBlock block)
		{
			context = context.WithNewCodeContext(CodeContext.EndHeader);
			using (PrintNode(context, block.Id, NodeKind.DoubleCircle))
			{
				TextFragment end = new("end", ClassificationKind.Keyword);
				TextFragmentLineCollection header = [];
				header.AddLine(end);

				PrintCode(context, header, TextAlign.Center);
			}
		}
		public void PrintBlock(Context context, IControlFlowExpressionBlock block)
		{
			using (PrintNode(context, block.Id, NodeKind.Rounded))
			{
				if (block.ConstructName is not null && (block.Expression.WillBranch is false))
				{
					// Note(Nightowl):
					// This assumes that the keyword will be the first thing in the parent;
					// Perhaps construct blocks should have a keyword token instead of a name?
					int line = block.Expression.Parent?.Position.Start.Line ?? block.Expression.Position.Start.Line;

					TextFragment name = new(block.ConstructName, ClassificationKind.Keyword);

					TextFragmentLineCollection nameLines = [new(line, name)];
					nameLines.PrefixLineMargin();

					PrintCode(context, nameLines, TextAlign.Center);
					// Note(Nightowl):
					// There might need to be a blank line here since the alignment
					// changes and that seems to mess things up but only sometimes?
				}

				TextFragmentLineCollection lines = block.Expression
					.GetLines()
					.TrimAll();

				PrintCode(context, lines);
			}
		}
		public void PrintBlock(Context context, IControlFlowStatementBlock block)
		{
			using (PrintNode(context, block.Id, NodeKind.Rectangle))
			{
				TextFragmentLineCollection lines = [];
				foreach (IAnnotatedStatementSyntax current in block.Statements)
					lines.AddRange(current.GetLines());

				lines
					.TrimAll()
					.PrefixLineMargin();

				PrintCode(context, lines);
			}
		}
		public void PrintBlock(Context context, IControlFlowConstructBlock block)
		{
			Debug.Assert(block.Expression is not null);
			using (PrintNode(context, block.Id, NodeKind.Rounded))
			{
				TextFragment name = new(block.Name, ClassificationKind.Keyword);

				TextFragmentLineCollection nameLines = [new(block.Node.Position.Start.Line, name)];
				nameLines.PrefixLineMargin();

				PrintCode(context, nameLines, TextAlign.Center);

				TextFragmentLineCollection lines = block
					.Expression
					.GetLines()
					.TrimAll();

				PrintCode(context, lines);
			}
		}
		public void PrintBlock(Context context, IControlFlowMarkerBlock block)
		{
			context.Writer.WriteLine("%% Marker blocks are not yet supported.");
		}
