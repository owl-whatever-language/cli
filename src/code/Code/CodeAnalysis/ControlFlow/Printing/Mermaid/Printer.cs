		#region Html methods
		public void PrintCode(Context context, TextFragmentLineCollection lines, TextAlign alignment = TextAlign.Left)
		{
			string alignmentValue = alignment switch
			{
				TextAlign.Left => "left",
				TextAlign.Center => "center",
				TextAlign.Right => "right",

				_ => ThrowHelper.ThrowArgumentException<string>(nameof(alignment), $"Unknown text alignment ({alignment}).")
			};

			List<string> styleParts =
			[
				"white-space:pre",
				$"text-align:{alignmentValue}"
			];

			if (context.Settings.TabSize is not null) // Note(Nightowl): Doesn't seem to always be respected?
				styleParts.Add($"tab-size:{context.Settings.TabSize}");

			if (context.Settings.IncludeLineNavigation)
				styleParts.Add($"margin:{context.Settings.LineNavigationPadding}px");

			string styleAttribute = $"style=\"{string.Join(';', styleParts)}\"";

			using (context.Writer.NoIndent())
			using (PrintElement(context, "div", [styleAttribute]))
				PrintCodeCore(context, lines);
		}
		public void PrintCodeCore(Context context, TextFragmentLineCollection lines)
		{
			for (int i = 0; i < lines.Count; i++)
			{
				TextFragmentLine line = lines[i];

				if (context.Settings.IncludeLineNavigation && context.CodeContext is CodeContext.SourceReference or CodeContext.Graph)
				{
					context.Writer.Write(' ');
					PrintLine(context, line);
					context.Writer.Write(' ');
				}
				else
					PrintLine(context, line);

				if (i + 1 < lines.Count)
					context.Writer.WriteLine();
			}
		}
		public void PrintLine(Context context, TextFragmentLine line)
		{
			bool isSimple =
				line.IsWhiteSpaceOrSourcePrefix ||
				line.IsOnlyCommented ||
				line.IsDimmed ||
				line.Line is null ||
				line.Count is 0 ||
				(context.Settings.IncludeLineData is false && context.Settings.IncludeLineNavigation is false);

			if (isSimple)
			{
				foreach (TextFragment fragment in line)
					PrintFragment(context, fragment);

				return;
			}

			Debug.Assert(line.Line is not null);
			Debug.Assert(line.Any());

			List<string> spanAttributes = [];

			if (context.Settings.IncludeLineData)
			{
				string attribute = GetLineDataAttribute(context, line.Line.Value);
				spanAttributes.Add(attribute);
			}

			if (context.Settings.IncludeLineNavigation)
			{
				string attribute = GetLineIdAttribute(context, line.Line.Value);
				spanAttributes.Add(attribute);
			}

			using (PrintElement(context, "span", spanAttributes))
			{
				foreach (TextFragment fragment in line)
				{
					if (TryGetLineLinkAttribute(context, fragment, out string? linkAttribute))
					{
						string styleAttribute = "style=\"text-decoration:none\"";

						using (PrintElement(context, "a", [linkAttribute, styleAttribute]))
							PrintFragment(context, fragment);
					}
					else
						PrintFragment(context, fragment);
				}
			}
		}
		public void PrintFragment(Context context, TextFragment fragment)
		{
			StyleInfo style = context.Settings.Styles?.Get(fragment.Classifications) ?? default;
			string value = EncodeValue(context, fragment.Text);

			List<string> attributes = [];

			if (style != default)
				attributes.Add(style.ToHtmlStyle);

			if (context.Settings.IncludeSymbolData && TryGetSymbolDataAttribute(context, fragment, out string? symbol))
				attributes.Add(symbol);

			if (attributes.Count is 0)
			{
				context.Writer.Write(value);
				return;
			}

			using (PrintElement(context, "span", attributes))
				context.Writer.Write(value);
		}
		public string EncodeValue(Context context, string value)
		{
			// Note(Nightowl): Because mermaid doesn't like semicolons;
			string[] parts = value.Split(';');

			for (int i = 0; i < parts.Length; i++)
				parts[i] = HttpUtility.HtmlEncode(parts[i]);

			string encoded = string.Join("&#x3b;", parts);

			// Note(Nightowl): Yet another thing that doesn't actually respect tab size, even though it's explicitly set;
			if (context.Settings.TabSize is not null)
			{
				string tabString = new(' ', context.Settings.TabSize.Value);
				encoded = encoded.Replace("\t", tabString);
			}

			return encoded;
		}
