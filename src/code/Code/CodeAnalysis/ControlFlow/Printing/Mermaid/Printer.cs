		#region Node methods
		public NodeScope PrintNode(Context context, string id, NodeKind kind)
		{
			(string prefix, string suffix) = kind switch
			{
				NodeKind.Rectangle => ("[", "]"), // ( )
				NodeKind.Rounded => ("(", ")"), // ( )
				NodeKind.Stadium => ("([", "])"), // ([ ])
				NodeKind.Circle => ("((", "))"), // (( ))
				NodeKind.DoubleCircle => ("(((", ")))"), // ((( )))
				NodeKind.Rhombus => ("{", "}"), // { }
				NodeKind.Hexagon => ("{{", "}}"), // {{ }}
				NodeKind.Parallelogram => ("[/", "/]"), // [/ /]
				NodeKind.Parallelogram2 => ("[\\", "\\]"), // [\ \]

				_ => ThrowHelper.ThrowArgumentException<(string, string)>(nameof(kind), $"Unknown node kind ({kind}).")
			};

			return PrintNode(context, id, prefix, suffix);
		}
		public NodeScope PrintNode(Context context, string id, string prefix, string suffix)
		{
			PrintNodeStart(context, id, prefix);
			return new(context, suffix);
		}
		public void PrintNodeStart(Context context, string id, string type)
